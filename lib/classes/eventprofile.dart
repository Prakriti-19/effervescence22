class Profile{
  final String name;
  final String time;
  final String details;
  final String venue;
  final String url;
  final String date;
  final String form;
  Profile(
      {
        required this.url,
        required this.form,
        required this.venue,
        required this.date,
        required this.time,
        required this.name,
        required this.details,
      });
}
